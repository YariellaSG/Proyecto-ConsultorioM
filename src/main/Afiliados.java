package main;
public class Afiliados {
    
      private int NAfiliado;
    private String NobraSocial;
    private int IDpaciente; 

    public Afiliados() {
        this.NAfiliado = 0;
        this.NobraSocial = "";
        this.IDpaciente = 0;
    }

    

    public int getNAfiliado() {
        return NAfiliado;
    }

    public void setNAfiliado(int NAfiliado) {
        this.NAfiliado = NAfiliado;
    }

    public String getNobraSocial() {
        return NobraSocial;
    }

    public void setNobraSocial(String NobraSocial) {
        this.NobraSocial = NobraSocial;
    }

    public int getIDpaciente() {
        return IDpaciente;
    }

    public void setIDpaciente(int IDpaciente) {
        this.IDpaciente = IDpaciente;
    }
    
    
    
}

    

